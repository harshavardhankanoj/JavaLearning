public class VariableDemo {
    public static void main(String[] args) {
        int num=45; //4 bytes
        short n=34; //2 bytes
        long l=2334234424l; //8 bytes
        float f=5355.3f; // 4 bytes
        double d=443.35; // 8 bytes
        char c='A';
        c=66;
        int b=(int)453.3;// type casting
        double e=443;//implicit conversion
        System.out.println(num+" "+n+" "+l+" "+f+" "+d+" "+c+" "+b+" "+e);
    }
}
